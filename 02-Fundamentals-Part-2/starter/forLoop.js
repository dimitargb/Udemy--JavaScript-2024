// for loop keeps wrunning whiel condition is true:

for (let rep = 1; rep <= 10; rep++) {
  console.log(`Lifting weights repetition ${rep} 🏋️`);
}

// Now let's use the counter variable rep = 1 to increase the value by one each time it loop.

for (let rep = 10; rep >= 1; rep--) {
  console.log(`Lifting weights repetition ${rep} 🏋️`);
}
